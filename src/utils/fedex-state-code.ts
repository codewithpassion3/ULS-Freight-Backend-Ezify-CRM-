export function toFedExStateCode(state: string | undefined): string {
  if (!state) return '';
  const normalized = state.trim().toLowerCase();
  
  // Already a 2-letter code?
  if (/^[a-z]{2}$/i.test(normalized)) {
    return normalized.toUpperCase();
  }
  
  return STATE_NAME_TO_CODE[normalized] || normalized.toUpperCase().slice(0, 2);
}